# Messages & Job Bus (全域狀態控管)
MESSAGE_BOARD: Dict[str, List[str]] = {}
board_lock = Lock() # for thread-safe access to MESSAGE_BOARD

JOB_LIST: List[Dict[str, Any]] = [] # 外部實例化的 job list
job_lock = Lock() # for thread-safe access to JOB_LIST

# Agent 溝通與狀態工具定義
@tool
def send_message(target_role: str, message: str) -> str:
    """發送訊息給其他協作的 Agent 角色（例如要求 API 規格或回報進度）。"""
    with board_lock:
        if target_role not in MESSAGE_BOARD:
            MESSAGE_BOARD[target_role] = []
        MESSAGE_BOARD[target_role].append(message)
    return f"已成功發送訊息給 {target_role}。"

@tool
def check_messages(my_role: str) -> str:
    """讀取發給自己的訊息。如果有其他 Agent 詢問你問題，請務必處理並回覆。"""
    with board_lock:
        if my_role not in MESSAGE_BOARD or not MESSAGE_BOARD[my_role]:
            return "目前沒有新訊息。"
        msgs = MESSAGE_BOARD[my_role].copy()
        MESSAGE_BOARD[my_role].clear()
        return "\n".join(msgs)

@tool
def wait_for_reply(my_role: str, wait_seconds: int = 5) -> str:
    """當你需要等待其他 Agent 提供資訊時，使用此工具暫停等待，隨後會回傳收到的訊息。"""
    time.sleep(wait_seconds)
    with board_lock:
        if my_role not in MESSAGE_BOARD or not MESSAGE_BOARD[my_role]:
            return "等待超時，目前尚未收到回覆，請決定是否繼續等待或先處理其他事。"
        msgs = MESSAGE_BOARD[my_role].copy()
        MESSAGE_BOARD[my_role].clear()
        return "\n".join(msgs)

@tool
def mark_job_finished(task_description: str) -> str:
    """當你完成一項被指派的具體任務時，呼叫此工具將該任務標記為完成 (finish=True)。"""
    with job_lock:
        for job in JOB_LIST:
            # 簡單字串比對，實務上可改用 ID
            if job['task_description'] == task_description:
                job['finish'] = True
                return f"任務 '{task_description}' 已成功標記為完成。"
        return f"找不到指定的任務 '{task_description}'，請確認任務描述是否完全一致。"

# 定義輸出格式
class TaskJob(BaseModel):
    task_description: str = Field(..., description="The specific job/task description")
    assigned_agent: str = Field(..., description="The role of the agent assigned to this job")

class AgentPlan(BaseModel):
    agent_count: int = Field(..., description="Agent count")
    agent_roles: List[str] = Field(..., description="Roles of the agents")
    agent_prompts: List[str] = Field(..., description="Prompts for the agents")
    jobs: List[TaskJob] = Field(..., description="List of specific jobs generated by the planner to be assigned to agents")
    
class PlanningResult(BaseModel):
    plan: AgentPlan = Field(..., description="The plan for the agents")
    supervisor_prompt: str = Field(..., description="The prompt for the supervisor")

# 定義狀態格式
class DynamicState(TypedDict):
    messages: Annotated[list, add_messages]
    agent_plan: AgentPlan | None
    supervisor_prompt: str | None
    final_result: str | None

# 定義單一Agent的狀態格式 (新增 assigned_jobs)
class AgentTaskState(TypedDict):
    role: str
    prompt: str
    task_description: str
    assigned_jobs: List[str] # 傳遞給 Agent 該負責的具體任務
    all_roles: List[str]

# 檔案操作 Tool定義
@tool
def execute_bash(command: str) -> str:
    """執行 Bash shell 指令並回傳標準輸出 (stdout) 或錯誤訊息 (stderr)。"""
    try:
        result = subprocess.run(
            command, shell=True, check=True, text=True, capture_output=True
        )
        return result.stdout if result.stdout else "指令執行成功，無輸出。"
    except subprocess.CalledProcessError as e:
        return f"指令執行失敗:\nError: {e.stderr}"

@tool
def read_file(file_path: str) -> str:
    """讀取指定檔案的內容。"""
    if not os.path.exists(file_path):
        return f"錯誤: 找不到檔案 {file_path}"
    try:
        with open(file_path, 'r', encoding='utf-8') as f:
            return f.read()
    except Exception as e:
        return f"讀取檔案發生錯誤: {str(e)}"

@tool
def write_file(file_path: str, content: str) -> str:
    """新建或覆寫指定檔案的內容。"""
    try:
        with open(file_path, 'w', encoding='utf-8') as f:
            f.write(content)
        return f"成功將內容寫入檔案 {file_path}"
    except Exception as e:
        return f"寫入檔案發生錯誤: {str(e)}"

@tool
def append_file(file_path: str, content: str) -> str:
    """在指定檔案的末尾追加內容。"""
    try:
        with open(file_path, 'a', encoding='utf-8') as f:
            f.write(content)
        return f"成功將內容追加到檔案 {file_path}"
    except Exception as e:
        return f"追加檔案發生錯誤: {str(e)}"

# 將 mark_job_finished 加入工具列
agent_tools = [execute_bash, read_file, write_file, append_file, send_message, check_messages, wait_for_reply, mark_job_finished]

