# Prompt 定義 (修改提示詞要求生成子任務)
planning_prompt = """你是一個專業的任務拆解與系統規劃專家。請將任務解構為多個子任務(jobs)，並指派給專屬 AI Agent。
請注意：這些 Agent 將會「平行且同時」執行。為確保他們能無縫協作，請針對會產生依賴的職責（例如前端依賴後端API）做好角色劃分與具體的 jobs 規劃，
他們將配備訊息通訊工具來解決依賴問題。
任務描述：{task_description}

請嚴格遵守以下 JSON 格式輸出，不要包含任何其他解釋文字或 Markdown 標記：
{{
  "plan": {{
    "agent_count": 2,
    "agent_roles": ["RoleA", "RoleB"],
    "agent_prompts": ["PromptA", "PromptB"],
    "jobs": [
      {{"task_description": "Job description", "assigned_agent": "RoleA"}}
    ]
  }},
  "supervisor_prompt": "Supervisor prompt here"
}}
"""

def agent_planner(state: DynamicState) -> PlanningResult:
    task_description = state['messages'][-1].content
    prompt = planning_prompt.format(task_description=task_description)
    
    print("\n--- [Planner] 開始規劃任務 ---")
    for _ in range(10):
        try:
            content = ""
            for chunk in model.stream([HumanMessage(content=prompt)]):
                print(chunk.content, end="", flush=True)
                content += chunk.content
            print("\n------------------------------\n")
            
            clean_json_str = re.sub(r'^```(?:json)?\n|\n```$', '', content.strip(), flags=re.MULTILINE)
            parsed_result = PlanningResult.model_validate_json(clean_json_str)
            break
        except:
            prompt = "以下的json輸出格式不正確，請按照json的格式再重新輸出一次。這是之前的輸出內容:\n" + content
    
    return {
        "agent_plan": parsed_result.plan,
        "supervisor_prompt": parsed_result.supervisor_prompt
    }

def dispatch_agents(state: DynamicState) -> List[Send]:
    plan = state["agent_plan"]
    task_description = state["messages"][-1].content
    all_roles = plan.agent_roles

    global MESSAGE_BOARD, JOB_LIST
    MESSAGE_BOARD = {}
    
    # 初始化外部的 Job List
    with job_lock:
        JOB_LIST = [
            {
                "task_description": job.task_description, 
                "assigned_agent": job.assigned_agent, 
                "finish": False
            } 
            for job in plan.jobs
        ]
    
    sends = []
    for role, prompt in zip(plan.agent_roles, plan.agent_prompts):
        # 篩選分配給該 Agent 的 Jobs
        assigned_jobs = [job.task_description for job in plan.jobs if job.assigned_agent == role]
        
        sends.append(Send("Executing", {
            "role": role, 
            "prompt": prompt, 
            "task_description": task_description,
            "assigned_jobs": assigned_jobs,
            "all_roles": all_roles
        }))
    return sends

def agent_executor(state: AgentTaskState) -> dict:
    jobs_str = "\n".join([f"- {job}" for job in state['assigned_jobs']]) if state['assigned_jobs'] else "無特定子任務，請依職責自由發揮。"
    
    system_instruction = f"""你扮演的角色是: {state['role']}
你的整體職責: {state['prompt']}

【你的待辦任務清單 (Jobs)】
{jobs_str}
---
【協作與通訊指示】
目前與你同時運行的同伴有：{', '.join(state['all_roles'])}
1. 若你缺乏某些資訊（例如需要別人決定 API 規格），請主動用 `send_message` 詢問負責的同伴，接著用 `wait_for_reply` 等待。
2. 執行任務期間，請適時呼叫 `check_messages` 檢查是否有人找你。若有，請優先回覆對方所需的資訊。
3. 檔案操作時，務必使用工具實際產生程式碼。
4. 當你完成任何一項「待辦任務清單」上的具體任務時，務必呼叫 `mark_job_finished` 將其標記為完成 (finish=True)。
"""
    agent = create_react_agent(
        model,
        tools=agent_tools,
        prompt=system_instruction
    )
    user_msg = HumanMessage(content=f"總體任務描述: {state['task_description']}")
    response = agent.invoke({"messages": [user_msg]})
    final_message = response["messages"][-1].content
    return {"messages": [f"【{state['role']}】的產出:\n{final_message}"]}

Graph = StateGraph(DynamicState)

Graph.add_node("Planning", agent_planner)
Graph.add_node("Executing", agent_executor)
Graph.add_edge(START, "Planning")
Graph.add_conditional_edges("Planning", dispatch_agents, ["Executing"])
Graph.add_edge("Executing", END)

app = Graph.compile()

